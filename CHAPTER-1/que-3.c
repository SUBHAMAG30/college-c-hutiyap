#include <stdio.h>

int main() {
    int first_num, second_num;
    first_num = 15;
    second_num = 4;
    printf("Summation of two number is %d", first_num + second_num);
    printf("Multiplication of two number is %d", first_num * second_num);
    printf("Remainder of two number is %d", first_num % second_num);
    printf("Subtraction of two number is %d", first_num - second_num);
    printf("Division of two number is %d", first_num / second_num);
    return 0;
}
